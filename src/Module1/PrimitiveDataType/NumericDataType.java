package Module1.PrimitiveDataType;

public class NumericDataType {

    // Integer Data Type
    public static byte byteDataType(){
        // 1 byte (8 bits)
        byte num = 1;
        return num;
    }

    public static short shortDataType(){
        // 2 bytes (16 bits)
        short num = 1;
        return num;
    }

    public static int integerDataType(){
        // 4 bytes (32 bits)
        int num = 1;
        return num;
    }

    public static long longDataType(){
        // 8 bytes (64 bits)
        long num = 12345678912L;
        return num;
    }

    // Real Number
    public static float floatDataType(){
        float num = 1;
        return num;
    }

    public static double doubleDataType(){
        double num = 1.0;
        return num;
    }
}
