package Module5.Collections;

public class ComparatorOperations {
    public void compareStrings(){
        System.out.println("Compare string");
        String first = "ABC";
        String second = "ADC";
        System.out.println("First with second");
        System.out.println(first.compareTo(second));
        System.out.println("Second with First");
        System.out.println(second.compareTo(first));
    }
}
